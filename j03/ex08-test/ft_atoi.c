int		ft_atoi(char *str)
{
	int nb;
	int neg;
	int a;
	
	nb = 0;
	neg = 1;
	a = 0;
	while (str[a] == ' ' || (str[a] >= '\t' && str[a] <= '\r')
			|| str[a] == '+'|| str[a] == '-')
	{	
		if (str[a] == '-')
			neg = -1;
		if ((str[a] == '+' || str[a] == '-') &&
			(str[a + 1] < '0' || str[a + 1] > '9'))
			return (0);
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
	{
		nb = (nb * 10) + str[a] - '0';
		a++;
	}
	return (nb * neg);
}
